#include <QTumorMapWidget.h>
#include <iostream>

///////////////////////////////////////////////////////////////////////////
QTumorMapWidget::QTumorMapWidget( QWidget * parent ) : QWidget( parent )
{
	_canvas = new QTumorMapCanvasWidget( this );
}

///////////////////////////////////////////////////////////////////////////
QTumorMapWidget::~QTumorMapWidget()
{
	delete _canvas;
}

///////////////////////////////////////////////////////////////////////////
QTumorMapCanvasWidget * QTumorMapWidget::canvas()
{
	return _canvas;
}

///////////////////////////////////////////////////////////////////////////
QSize QTumorMapWidget::sizeHint() const
{
	return QSize( 300, 150 );
};

///////////////////////////////////////////////////////////////////////////
QSizePolicy QTumorMapWidget::sizePolicy()
{
	return QSizePolicy( QSizePolicy::Minimum, QSizePolicy::Minimum );
}
